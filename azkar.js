'use strict';

let _currentAzkarCat = null;
let _azkarCounts = {};

/* ══ فتح فئة أذكار (old-style, maps to new HTML IDs) ══ */
function openAzkarCategory(cat) {
  /* Delegate to the newer openZikr() which uses correct HTML IDs */
  openZikr(cat);
}

/* ══ الضغط على الذكر ══ */
function zikrTap(i) {
  _azkarCounts[i] = (_azkarCounts[i] || 0) + 1;
  const el = document.getElementById('zc_' + i);
  if (el) {
    el.textContent = _azkarCounts[i];
    el.classList.remove('bumped');
    void el.offsetWidth;
    el.classList.add('bumped');
    setTimeout(() => el.classList.remove('bumped'), 350);
  }
  const st = loadState();
  st.xp = (st.xp || 0) + 1;
  saveState(st);
}

/* ══ تمييز الذكر كمكتمل ══ */
function zikrMarkDone(i) {
  const el = document.getElementById('zc_' + i);
  if (el) el.style.color = '#059669';
  showToast('✅ أحسنت! تقبل الله', 'success');
}

/* ══ إعادة تعيين عرض الأذكار (maps to new HTML IDs) ══ */
function resetAzkarView() {
  _currentAzkarCat = null;
  _azkarCounts = {};
  /* Use the correct HTML IDs */
  const catView  = document.getElementById('azkarCatView');
  const zikrView = document.getElementById('zikrView');
  if (catView)  catView.style.display  = 'block';
  if (zikrView) zikrView.style.display = 'none';
}

/* ══ زر الرجوع في الأذكار ══ */
function azkarGoBack() {
  if (_currentAzkarCat) {
    closeZikr();
  } else {
    showScreen('homeScreen');
  }
}

/* ══ بناء الفئات (graceful fallback) ══ */
function renderAzkarCategories() {
  /* The HTML already has static category buttons; this is a no-op fallback */
  const container = document.getElementById('azkarCatView');
  if (!container) return;
  /* Only inject if the static HTML buttons are missing */
  if (container.querySelector('.azkar-cat-btn')) return;
}

